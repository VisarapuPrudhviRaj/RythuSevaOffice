package nk.bluefrog.rythusevaoffice.models;

public class Gp {

    private String id;
    private String name;
    private String mpoName;
    private String mpoMobile;
    private String farmerCount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMpoName() {
        return mpoName;
    }

    public void setMpoName(String mpoName) {
        this.mpoName = mpoName;
    }

    public String getMpoMobile() {
        return mpoMobile;
    }

    public void setMpoMobile(String mpoMobile) {
        this.mpoMobile = mpoMobile;
    }

    public String getFarmerCount() {
        return farmerCount;
    }

    public void setFarmerCount(String farmerCount) {
        this.farmerCount = farmerCount;
    }
}
